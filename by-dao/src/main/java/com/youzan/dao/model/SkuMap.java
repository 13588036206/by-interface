package com.youzan.dao.model;

public class SkuMap {
    private String k;
    private String kid;
    private String v;
    private String vid;

    public String getK() {
        return k;
    }

    public void setK(String k) {
        this.k = k;
    }

    public String getKid() {
        return kid;
    }

    public void setKid(String kid) {
        this.kid = kid;
    }

    public String getV() {
        return v;
    }

    public void setV(String v) {
        this.v = v;
    }

    public String getVid() {
        return vid;
    }

    public void setVid(String vid) {
        this.vid = vid;
    }
}
